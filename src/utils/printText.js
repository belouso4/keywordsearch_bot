    let channels = ''
    user.channels.forEach(channel => {
        channels += '@' + channel.replace(/_/g, "\\\_") + '\n'
    })
    let keywords = ''
    user.keywords.forEach(keyword => {
        keywords += keyword + '\n'
    })

    let text = `*–ë–æ—Ç –ø–æ–º–æ–≥–∞–µ—Ç –º–æ–Ω–∏—Ç–æ—Ä–∏—Ç—å –Ω–æ–≤—ã–µ –∑–∞–ø–∏—Å–∏ –≤ –∫–∞–Ω–∞–ª–∞—Ö –∏ —á–∞—Ç–∞—Ö –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º.*`

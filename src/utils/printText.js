    text += `‚Ä¢ –î–æ–±–∞–≤—å—Ç–µ –∫–∞–Ω–∞–ª—ã –∏ –≥—Ä—É–ø–ø—ã, –∫–æ—Ç–æ—Ä—ã–µ –±–æ—Ç –¥–æ–ª–∂–µ–Ω –æ—Ç—Å–º–∞—Ç—Ä–∏–≤–∞—Ç—å —Å –ø–æ–º–æ—â—å—é –∫–Ω–æ–ø–∫–∏ –ö–∞–Ω–∞–ª—ã`

    text += `–î–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –±–æ—Ç–∞ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ:`
    text += `\n\n`
    text += `‚Ä¢ –î–æ–±–∞–≤–∏—Ç—å —Å–ª–æ–≤–∞, –∫–æ—Ç–æ—Ä—ã–µ –±—É–¥–µ–º –æ—Ç—Å–ª–µ–∂–∏–≤–∞—Ç—å, —Å –ø–æ–º–æ—â—å—é –∫–Ω–æ–ø–∫–∏ –°–ª–æ–≤–∞`
